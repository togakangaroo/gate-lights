def breathing_pattern():
    """
    Breathing pattern - gradually light up and down like breathing.
    """
    while True:
        # Breathing in - gradually light up all lights
        yield ("""000000
                  000000""", 300)
        yield ("""000000
                  010000""", 300)
        yield ("""000000
                  011000""", 300)
        yield ("""010000
                  011000""", 300)
        yield ("""011000
                  011100""", 300)
        yield ("""011000
                  111100""", 300)
        yield ("""011100
                  111100""", 300)
        yield ("""111100
                  111110""", 300)
        yield ("""111100
                  111111""", 300)
        yield ("""111110
                  111111""", 300)
        yield ("""111111
                  111111""", 300)
        # Hold
        yield ("""111111
                  111111""", 300)
        yield ("""111111
                  111111""", 300)
        # Breathing out - gradually turn off
        yield ("""111110
                  111111""", 300)
        yield ("""111100
                  111111""", 300)
        yield ("""111100
                  111110""", 300)
        yield ("""111100
                  111100""", 300)
        yield ("""011100
                  111100""", 300)
        yield ("""011000
                  111100""", 300)
        yield ("""011000
                  011100""", 300)
        yield ("""010000
                  011000""", 300)
        yield ("""000000
                  011000""", 300)
        yield ("""000000
                  010000""", 300)
        yield ("""000000
                  000000""", 300)
        # Hold
        yield ("""000000
                  000000""", 300)
        yield ("""000000
                  000000""", 300)
